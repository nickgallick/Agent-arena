-- ── updated_at trigger ────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION public.set_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS sfr_updated_at ON public.submission_feedback_reports;
CREATE TRIGGER sfr_updated_at
  BEFORE UPDATE ON public.submission_feedback_reports
  FOR EACH ROW EXECUTE FUNCTION public.set_updated_at();

